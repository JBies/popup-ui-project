export function t(key) {
  const lang = getCurrentLanguage();
  return (translations[lang] && translations[lang][key]) || (translations.en[key]) || key;
}

export function getCurrentLanguage() {
  return localStorage.getItem('language') || 'en';
}

export function toggleLanguage() {
  const next = getCurrentLanguage() === 'en' ? 'fi' : 'en';
  localStorage.setItem('language', next);
  applyTranslations();
  updateLangButton();
  window.dispatchEvent(new CustomEvent('languagechange', { detail: { lang: next } }));
  // Persist to account only if logged in (skip silently if not)
  if (window._i18nUserLoggedIn) {
    fetch('/api/user/language', {
      method: 'PATCH',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ language: next })
    }).catch(() => {});
  }
}

export function initLanguage() {
  applyTranslations();
  updateLangButton();
}

// Fetch language preference from server for logged-in users.
// Overrides localStorage so preference follows the account across browsers.
export async function syncLanguageFromAccount() {
  try {
    const res = await fetch('/api/user');
    if (!res.ok) return;
    const data = await res.json();
    if (!data?.user) return;
    window._i18nUserLoggedIn = true;
    const lang = data.user.language;
    if (lang && ['en', 'fi'].includes(lang)) {
      const prev = localStorage.getItem('language') || 'en';
      localStorage.setItem('language', lang);
      applyTranslations();
      updateLangButton();
      if (lang !== prev) {
        window.dispatchEvent(new CustomEvent('languagechange', { detail: { lang } }));
      }
    }
  } catch (_) {}
}

function applyTranslations() {
  const lang = getCurrentLanguage();
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const val = (translations[lang] && translations[lang][key]) || translations.en[key];
    if (val !== undefined) el.textContent = val;
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.dataset.i18nPlaceholder;
    const val = (translations[lang] && translations[lang][key]) || translations.en[key];
    if (val !== undefined) el.placeholder = val;
  });
}

function updateLangButton() {
  document.querySelectorAll('.lang-toggle').forEach(btn => {
    const lang = getCurrentLanguage();
    btn.textContent = lang === 'en' ? 'FI' : 'EN';
    btn.setAttribute('aria-label', lang === 'en' ? 'Switch to Finnish' : 'Switch to English');
  });
}

// Auto-init for non-module pages
if (typeof document !== 'undefined') {
  document.addEventListener('DOMContentLoaded', () => {
    initLanguage();
    // Sync from account (async — updates UI once server responds)
    syncLanguageFromAccount();
    document.querySelectorAll('.lang-toggle').forEach(btn => {
      btn.addEventListener('click', toggleLanguage);
    });
  });
}
